#include <iostream>
using namespace std;

// ADICAO
double Adicao(double x){
    double NumeroDois = 0;
    cin >> NumeroDois;

    return x + NumeroDois;
}

// SUBTRACAO
double Subtracao(double x){
    double NumeroDois = 0;
    cin >> NumeroDois;

    return x - NumeroDois;
}

// DIVISAO
double Divisao(double x){
    double NumeroDois = 0;
    cin >> NumeroDois;

    return x / NumeroDois;
}

// MULTIPLICACAO
double Multiplicacao(double x){
    double NumeroDois = 0;
    cin >> NumeroDois;

    return x * NumeroDois;
}